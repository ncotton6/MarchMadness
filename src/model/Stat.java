package model;

/**
 * {@link Stat} object is a general non-team specific object for holding data
 * for a class of data (i.e. winners/losers).
 * 
 * @author Nathaniel Cotton
 * 
 */
public class Stat {

	private double fgm, fga, fgm3, fga3, ftm, fta, or, dr, ast, to, stl, blk,
			pf;

	/**
	 * @return the fgm
	 */
	public double getFgm() {
		return fgm;
	}

	/**
	 * @param fgm
	 *            the fgm to set
	 */
	public void setFgm(double fgm) {
		this.fgm = fgm;
	}

	/**
	 * @return the fga
	 */
	public double getFga() {
		return fga;
	}

	/**
	 * @param fga
	 *            the fga to set
	 */
	public void setFga(double fga) {
		this.fga = fga;
	}

	/**
	 * @return the fgm3
	 */
	public double getFgm3() {
		return fgm3;
	}

	/**
	 * @param fgm3
	 *            the fgm3 to set
	 */
	public void setFgm3(double fgm3) {
		this.fgm3 = fgm3;
	}

	/**
	 * @return the fga3
	 */
	public double getFga3() {
		return fga3;
	}

	/**
	 * @param fga3
	 *            the fga3 to set
	 */
	public void setFga3(double fga3) {
		this.fga3 = fga3;
	}

	/**
	 * @return the ftm
	 */
	public double getFtm() {
		return ftm;
	}

	/**
	 * @param ftm
	 *            the ftm to set
	 */
	public void setFtm(double ftm) {
		this.ftm = ftm;
	}

	/**
	 * @return the fta
	 */
	public double getFta() {
		return fta;
	}

	/**
	 * @param fta
	 *            the fta to set
	 */
	public void setFta(double fta) {
		this.fta = fta;
	}

	/**
	 * @return the or
	 */
	public double getOr() {
		return or;
	}

	/**
	 * @param or
	 *            the or to set
	 */
	public void setOr(double or) {
		this.or = or;
	}

	/**
	 * @return the dr
	 */
	public double getDr() {
		return dr;
	}

	/**
	 * @param dr
	 *            the dr to set
	 */
	public void setDr(double dr) {
		this.dr = dr;
	}

	/**
	 * @return the ast
	 */
	public double getAst() {
		return ast;
	}

	/**
	 * @param ast
	 *            the ast to set
	 */
	public void setAst(double ast) {
		this.ast = ast;
	}

	/**
	 * @return the to
	 */
	public double getTo() {
		return to;
	}

	/**
	 * @param to
	 *            the to to set
	 */
	public void setTo(double to) {
		this.to = to;
	}

	/**
	 * @return the stl
	 */
	public double getStl() {
		return stl;
	}

	/**
	 * @param stl
	 *            the stl to set
	 */
	public void setStl(double stl) {
		this.stl = stl;
	}

	/**
	 * @return the blk
	 */
	public double getBlk() {
		return blk;
	}

	/**
	 * @param blk
	 *            the blk to set
	 */
	public void setBlk(double blk) {
		this.blk = blk;
	}

	/**
	 * @return the pf
	 */
	public double getPf() {
		return pf;
	}

	/**
	 * @param pf
	 *            the pf to set
	 */
	public void setPf(double pf) {
		this.pf = pf;
	}
}
